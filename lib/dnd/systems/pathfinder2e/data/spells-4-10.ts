// lib/dnd/systems/pathfinder2e/data/spells-4-10.ts — the PF2 (Remaster) SPELL catalog for ranks
// 4–10, plus the FOCUS spells, authored against `PF2SpellFull` in ../defs.ts.
//
// WHY THIS FILE EXISTS: it is the second tranche of the spell catalog. spells-0-3.ts covers cantrips
// and ranks 1–3; this covers the top half of the ranked list and the focus spells, which are a
// different animal entirely — a focus spell is not cast from a slot, it belongs to a CLASS, and the
// eligibility gate judges it on `focusClass` ownership alone (see ../eligibility.ts). That is why
// focus entries live here with the ranked ones rather than in a class file: they are still spells,
// they just answer a different question.
//
// LICENSING: Pathfinder 2e's MECHANICS are ORC-licensed, which expressly permits reproducing rules.
// Reserved Material — Paizo trademarks, deities, characters, locations, lore, art — appears nowhere
// here. Divine focus spells in particular are deity-adjacent in the source books; only their
// MECHANICS are recorded, never a deity, a faith, or its lore. Every `effect` is a paraphrased
// mechanical summary written for this file; no rulebook prose is reproduced. Every entry carries
// `source`.
//
// GROUND RULE — NEVER INVENT A RULE. A wrong TRADITION is the worst failure available here: it does
// not throw, it silently makes the eligibility gate refuse legal spells or admit illegal ones. So
// where a spell's rank, tradition list, or remaster name could not be stated with confidence the
// spell is LEFT OUT ENTIRELY, and where a single field (a damage die, an area, a heightening line)
// was uncertain that FIELD is omitted while the rest of the entry stands. This catalog is therefore
// much smaller than the published lists by design — PF2_SPELLS_R4_10_STATUS at the foot of the file
// reports the gaps honestly. Never pad it.
//
// SOURCE HONESTY: entries marked 'Legacy' are authored from the pre-remaster printing because the
// remaster status of that specific entry (its name, or whether it survived at all) could not be
// confirmed. They are still mechanically usable; they are flagged so a reader knows which shelf they
// came off. Entries marked 'Player Core' / 'Player Core 2' are ones whose remastered form is known.
//
// REMASTER VOCABULARY: rank (not spell level), void (not negative), vitality (not positive),
// off-guard (not flat-footed). `formerly` preserves pre-remaster names so old character sheets and
// player searches still resolve.
