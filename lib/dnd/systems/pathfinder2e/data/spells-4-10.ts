/** Case-insensitive lookup by current name OR pre-remaster name (`formerly`), across both the
 *  ranked tranche and the focus spells — same contract as `pf2SpellR0_3`. */
export function pf2SpellR4_10(name: string): PF2SpellFull | null {
  const key = name.trim().toLowerCase();
  return (
    PF2_SPELLS_R4_10_ALL.find(s => s.name.toLowerCase() === key) ||
    PF2_SPELLS_R4_10_ALL.find(s => s.formerly?.toLowerCase() === key) ||
    null
  );
}

/** Ranked spells at a given rank. Focus spells are excluded on purpose: a focus spell's rank is not
 *  a slot the player spends, so listing it beside rank-4 slot spells would be misleading. */
export const pf2RankedSpellsOfRank = (rank: number): PF2SpellFull[] =>
  PF2_SPELLS_R4_10.filter(s => s.rank === rank);

/** Ranked spells on a tradition — the list the eligibility gate filters a repertoire against. */
export const pf2SpellsR4_10OfTradition = (tradition: string): PF2SpellFull[] => {
  const key = tradition.trim().toLowerCase();
  return PF2_SPELLS_R4_10.filter(s => s.traditions.some(t => t === key));
};

/** Every focus spell belonging to one class. Case-insensitive; this is the list the sheet shows a
 *  character alongside their Focus Points. */
export const pf2FocusSpellsOfClass = (className: string): PF2SpellFull[] => {
  const key = className.trim().toLowerCase();
  return PF2_FOCUS_SPELLS.filter(s => s.focusClass?.toLowerCase() === key);
};

