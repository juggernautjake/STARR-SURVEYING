// ── Focus spells ──────────────────────────────────────────────────────────────────────────────
// Focus spells are cast from Focus Points and belong to a CLASS, so the eligibility gate judges
// them on `focusClass` and never on a slot ceiling — a level-1 character's focus spell is legal.
// They also heighten automatically to half the caster's level rounded up, so a `heightened` line
// below describes the automatic increment, not a slot the player chooses to spend.
//
// TRADITIONS: a focus spell normally uses its class's tradition, and that is what is recorded. Where
// the tradition genuinely depends on a character choice (a witch's patron, a sorcerer's bloodline)
// or the spell has no tradition at all (monk qi spells), `traditions` is left EMPTY rather than
// guessed — an empty list is "not fixed by the spell", and the focus branch of the gate never reads
// it. Bloodline entries whose tradition IS fixed by the bloodline record that tradition.

