// Each background grants two attribute boosts — ONE chosen from `attributeChoice`, plus one free —
// trains one or two skills including a Lore, and grants a skill feat.
//
// The inherited `boosts` field keeps the seed's `[first, 'free']` shape so nothing that reads the seed
// breaks; `attributeChoice` is the field that states the rule correctly. A builder should offer the
// choice, not silently assign the first option.
//
// Lore subcategories that are chosen at the table (a terrain, a settlement, a faith) are recorded as a
// generic label plus a `loreNote`, never as a specific place or deity — those are Reserved Material.

const bg = (
  name: string,
  attributeChoice: PF2AttributeKey[],
  skills: string[],
  lore: string,
  feats: string[],
  summary: string,
  extra: Partial<PF2BackgroundFull> = {},
): PF2BackgroundFull => ({
  name,
  attributeChoice,
  boosts: [attributeChoice[0], 'free'],
  skills,
  skill: skills[0],
  lore,
  feats,
  feat: feats[0],
  summary,
  source: 'Player Core',
  ...extra,
});

