// ── Lookups ───────────────────────────────────────────────────────────────────────────────────────
// Same contract as content.ts: case- and whitespace-insensitive, returning null rather than undefined.

const key = (s: unknown) => String(s ?? '').trim().toLowerCase();

const ANCESTRY_BY_NAME = new Map(PF2_ANCESTRIES_FULL.map((a) => [key(a.name), a]));
const HERITAGE_BY_NAME = new Map(PF2_HERITAGES.map((h) => [key(h.name), h]));
const ANCESTRY_FEAT_BY_NAME = new Map(PF2_FEATS_ANCESTRY.map((f) => [key(f.name), f]));
const BACKGROUND_BY_NAME = new Map(PF2_BACKGROUNDS_FULL.map((b) => [key(b.name), b]));

export function pf2AncestryFull(name: string): PF2AncestryFull | null {
  return ANCESTRY_BY_NAME.get(key(name)) ?? null;
}

export function pf2Heritage(name: string): PF2HeritageDef | null {
  return HERITAGE_BY_NAME.get(key(name)) ?? null;
}

export function pf2AncestryFeat(name: string): PF2FeatFull | null {
  return ANCESTRY_FEAT_BY_NAME.get(key(name)) ?? null;
}

export function pf2BackgroundFull(name: string): PF2BackgroundFull | null {
  return BACKGROUND_BY_NAME.get(key(name)) ?? null;
}

/** Every heritage a character of this ancestry may choose — its own, plus the versatile heritages
 *  (Aiuvarin, Dromaar, Changeling, Nephilim) which are stored under `ancestry: 'Any'`. Filtering
 *  `PF2_HERITAGES` on the ancestry name alone would silently hide those four. */
export function pf2HeritagesFor(ancestry: string): PF2HeritageDef[] {
  const want = key(ancestry);
  if (!want) return [];
  return PF2_HERITAGES.filter((h) => key(h.ancestry) === want || key(h.ancestry) === 'any');
}

/** Every ancestry feat scoped to this ancestry, optionally capped at a character level. The cap is a
 *  convenience for pickers; the real gate is `pf2FeatEligibility`, which also checks prerequisites. */
export function pf2AncestryFeatsFor(ancestry: string, throughLevel?: number): PF2FeatFull[] {
  const want = key(ancestry);
  if (!want) return [];
  return PF2_FEATS_ANCESTRY.filter(
    (f) => key(f.ancestry) === want && (throughLevel == null || f.level <= throughLevel),
  );
}

