/** Case-insensitive lookup by current name OR pre-remaster name (`formerly`), so a sheet written
 *  before the remaster still resolves "Magic Missile" to Force Barrage. */
export function pf2SpellR0_3(name: string): PF2SpellFull | null {
  const key = name.trim().toLowerCase();
  return (
    PF2_SPELLS_R0_3.find(s => s.name.toLowerCase() === key) ||
    PF2_SPELLS_R0_3.find(s => s.formerly?.toLowerCase() === key) ||
    null
  );
}

/** Everything at a given rank (0 = cantrips). */
export const pf2SpellsOfRank = (rank: number): PF2SpellFull[] =>
  PF2_SPELLS_R0_3.filter(s => s.rank === rank);

/** Everything a tradition can cast — the list the eligibility gate filters a repertoire against. */
export const pf2SpellsOfTradition = (tradition: string): PF2SpellFull[] => {
  const key = tradition.trim().toLowerCase();
  return PF2_SPELLS_R0_3.filter(s => s.traditions.some(t => t === key));
};

