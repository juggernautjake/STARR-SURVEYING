// worker/src/infra/site-health-monitor.ts — Site Health Monitor
// Probes all county CAD portals, clerk systems, and external data sources to
// detect when HTML structure or selectors have changed.  Broadcasts alerts
// via WebSocket and exposes REST endpoints for the admin dashboard.
//
// Core capabilities:
//   1. Periodic health checks (configurable interval, default 30 min)
//   2. Selector validation — checks that expected DOM elements exist
//   3. Per-vendor and per-county health status
//   4. WebSocket notification when a site goes down or selectors drift
//   5. "Check All" endpoint to run every probe at once
//   6. Screenshot capture on failure for diagnostics

import type { Browser, Page } from 'playwright';
import { acquireBrowser } from '../lib/browser-factory.js';
import { getCADConfig, listRegisteredCounties, type CADConfig } from '../services/cad-registry.js';

// ── Types ───────────────────────────────────────────────────────────────────

export type SiteStatus = 'healthy' | 'degraded' | 'down' | 'unchecked';
export type AlertSeverity = 'info' | 'warning' | 'error';

export interface SelectorProbe {
  /** CSS selector to look for */
  selector: string;
  /** Human-readable description */
  label: string;
  /** Is this selector required for the adapter to work? */
  required: boolean;
}

export interface SiteHealthResult {
  /** Site identifier (e.g. "hcad", "bell-bis", "kofile-bell") */
  siteId: string;
  /** Display name */
  name: string;
  /** Vendor type */
  vendor: string;
  /** URL that was probed */
  url: string;
  /** Overall status */
  status: SiteStatus;
  /** When this check ran */
  checkedAt: string;
  /** How long the probe took (ms) */
  latencyMs: number;
  /** Individual selector results */
  selectors: SelectorCheckResult[];
  /** Alerts generated by this check */
  alerts: SiteAlert[];
  /** Screenshot path if captured (only on failure) */
  screenshotPath?: string;
  /** Error message if the probe itself failed */
  error?: string;
}

export interface SelectorCheckResult {
  selector: string;
  label: string;
  required: boolean;
  found: boolean;
  /** Number of elements matched */
  count: number;
}

export interface SiteAlert {
  type: 'selector_missing' | 'selector_changed' | 'site_unreachable' | 'slow_response' | 'cloudflare_blocked';
  severity: AlertSeverity;
  message: string;
  siteId: string;
  timestamp: string;
}

export interface HealthSummary {
  totalSites: number;
  healthy: number;
  degraded: number;
  down: number;
  unchecked: number;
  lastFullCheck: string | null;
  alerts: SiteAlert[];
  sites: SiteHealthResult[];
}

