describe('Slice 229 — CanvasViewport render path', () => {
  const SRC = fs.readFileSync(
    path.join(__dirname, '..', '..', '..', 'app', 'admin', 'cad', 'components', 'CanvasViewport.tsx'),
    'utf8',
  );

  it('imports the annotation store', () => {
    expect(SRC).toMatch(/useAnnotationStore,/);
  });

  it('adds an areaLabelTexts Map onto the Pixi state', () => {
    expect(SRC).toMatch(/areaLabelTexts: Map<string, import\('pixi\.js'\)\.Text>/);
    expect(SRC).toMatch(/areaLabelTexts: new Map\(\)/);
  });

  it('defines renderAreaAnnotations + invokes it from the per-frame pipeline', () => {
    expect(SRC).toMatch(/function renderAreaAnnotations\(\) \{/);
    // Sandwiched between renderLabels and renderTextFeatures so it
    // sits on the labelLayer with the other labels.
    expect(SRC).toMatch(/renderLabels\(\);\s*\n\s*\/\/ Slice 229[\s\S]*?renderAreaAnnotations\(\);\s*\n\s*renderTextFeatures\(\);/);
  });

  it('renderAreaAnnotations GCs Pixi text objects whose annotations were removed', () => {
    // Locks the alive-id sweep so a future refactor can't drop the
    // cleanup and start leaking Pixi Text objects.
    expect(SRC).toMatch(/if \(!aliveIds\.has\(id\)\) \{/);
    expect(SRC).toMatch(/pixi\.areaLabelTexts\.delete\(id\);/);
  });

  it('renderAreaAnnotations only draws annotations whose type === AREA_LABEL + visible !== false', () => {
    expect(SRC).toMatch(/if \(!ann \|\| ann\.type !== 'AREA_LABEL'\) continue;/);
    expect(SRC).toMatch(/if \(ann\.visible === false\) continue;/);
  });
});
